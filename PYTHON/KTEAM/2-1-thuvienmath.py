import math

#tra ve mot so nguyen la phan nguyen cua so x
print(math.trunc(-3.999))

# tra ve mot so nguyen duoc lam tron tu x luon NHO hon hoac bang x(hay dung cho so am)
print(math.floor(-3.999))

# tra ve mot so nguyen duoc lam tron tu x luon LON hon hoac bang x(hay dung cho so am)
print(math.ceil(-3.999))

#tra ve 1 so thuc la tri tuyet doi cua x
print(math.fabs(-3.999))

#lay can bac 2
print(math.sqrt(16))

#tim uoc chung lon nhat
print(math.gcd(4,6))